  suspend fun ICACSRRequest(timedInvokeTimeout: Duration? = null): ICACSRResponse {
    val commandId: UInt = 0u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout,
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_NOC_ISSUER_CSR: Int = 0
    var NOCIssuerCSR_decoded: ByteArray? = null

    while (!tlvReader.isEndOfContainer()) {
      val tag = tlvReader.peekElement().tag

      if (tag == ContextSpecificTag(TAG_NOC_ISSUER_CSR)) {
        NOCIssuerCSR_decoded = tlvReader.getByteArray(tag)
      } else {
        tlvReader.skipElement()
      }
    }

    if (NOCIssuerCSR_decoded == null) {
      throw IllegalStateException("NOCIssuerCSR not found in TLV")
    }

    tlvReader.exitContainer()

    return ICACSRResponse(NOCIssuerCSR_decoded)
  }

  suspend fun addICAC(
    statusCode: UByte,
    NOCIssuerCert: ByteArray,
    nodeId: ULong,
    fabricId: ULong,
    adminVendorId: UShort,
    caseAdminSubject: ULong,
    timedInvokeTimeout: Duration? = null,
  ): AddICACResponse {
    val commandId: UInt = 2u

    val tlvWriter = TlvWriter()
    tlvWriter.startStructure(AnonymousTag)

    val TAG_STATUS_CODE_REQ: Int = 0
    tlvWriter.put(ContextSpecificTag(TAG_STATUS_CODE_REQ), statusCode)

    val TAG_NOC_ISSUER_CERT_REQ: Int = 1
    tlvWriter.put(ContextSpecificTag(TAG_NOC_ISSUER_CERT_REQ), NOCIssuerCert)

    val TAG_NODE_ID_REQ: Int = 2
    tlvWriter.put(ContextSpecificTag(TAG_NODE_ID_REQ), nodeId)

    val TAG_FABRIC_ID_REQ: Int = 3
    tlvWriter.put(ContextSpecificTag(TAG_FABRIC_ID_REQ), fabricId)

    val TAG_ADMIN_VENDOR_ID_REQ: Int = 4
    tlvWriter.put(ContextSpecificTag(TAG_ADMIN_VENDOR_ID_REQ), adminVendorId)

    val TAG_CASE_ADMIN_SUBJECT_REQ: Int = 5
    tlvWriter.put(ContextSpecificTag(TAG_CASE_ADMIN_SUBJECT_REQ), caseAdminSubject)
    tlvWriter.endStructure()

    val request: InvokeRequest =
      InvokeRequest(
        CommandPath(endpointId, clusterId = CLUSTER_ID, commandId),
        tlvPayload = tlvWriter.getEncoded(),
        timedRequest = timedInvokeTimeout,
      )

    val response: InvokeResponse = controller.invoke(request)
    logger.log(Level.FINE, "Invoke command succeeded: ${response}")

    val tlvReader = TlvReader(response.payload)
    tlvReader.enterStructure(AnonymousTag)
    val TAG_STATUS_CODE: Int = 0
    var statusCode_decoded: UByte? = null

    val TAG_FABRIC_INDEX: Int = 1
    var fabricIndex_decoded: UByte? = null

    val TAG_DEBUG_TEXT: Int = 2
    var debugText_decoded: String? = null

    while (!tlvReader.isEndOfContainer()) {
      val tag = tlvReader.peekElement().tag

      if (tag == ContextSpecificTag(TAG_STATUS_CODE)) {
        statusCode_decoded = tlvReader.getUByte(tag)
      }

      if (tag == ContextSpecificTag(TAG_FABRIC_INDEX)) {
        fabricIndex_decoded =
          if (tlvReader.isNull()) {
            tlvReader.getNull(tag)
            null
          } else {
            if (tlvReader.isNextTag(tag)) {
              tlvReader.getUByte(tag)
            } else {
              null
            }
          }
      }

      if (tag == ContextSpecificTag(TAG_DEBUG_TEXT)) {
        debugText_decoded =
          if (tlvReader.isNull()) {
            tlvReader.getNull(tag)
            null
          } else {
            if (tlvReader.isNextTag(tag)) {
              tlvReader.getString(tag)
            } else {
              null
            }
          }
      } else {
        tlvReader.skipElement()
      }
    }

    if (statusCode_decoded == null) {
      throw IllegalStateException("statusCode not found in TLV")
    }

    tlvReader.exitContainer()

    return AddICACResponse(statusCode_decoded, fabricIndex_decoded, debugText_decoded)
  }

